# >> return value
#    1) assume start cell is not blocked
#    2) if the end is reached, return agent_position = end
#    3) if a blocked cell is encountered, return agent_position right before the blocked cell
# >> move along the path, and stop if it hits a blocked cell: 
#    1) remove actions to and from the blocked cell
#       (1) remove actions "to the blocked cell" from its 4 neighbor cells  
#       (2) remove 4 actions "from the blocked cell" 
#       ** use the "remove actions" approach instead of "set action cost to infinity"
#          this way, the blocked cell will not be added to the open_list with g(s) = infinity
#          to be involved in open_list operations; this helps time and space
#          gen_maze() generates the maze with a certain percentage of blocked cells
#    2) revise heuritics h(s) for all nodes in closed_list (for adaptive A*)
#       >> to make h(s) more accurate
#       >> for later search "efficiency", expect less expanded nodes
